valor=bin(10)
print("bin(10):",valor)

valor=int('0b1010',2)
print("int('0b1010',2):",valor)

valor=hex(10)
print("hex(10)",valor)

valor=int('0xa',16)
print("int('0xa')",valor)

valor=abs(-5)
print("abs(-5)",valor)

valor=round(6.4)
print("round(6.4)",valor)

valor=eval('5+6')
print("eval('5+6')",valor)


