'''
Declarar múltiples variables.
Pedir al usuario que ingrese el valor de 2 variables
Realizar varias operaciones aritméticas (Suma,Resta, Division y Multiplicacion).
Mostrar el resultado en consola con print.
'''
n1=float(input("Humano inresa el numero 1: "))
n2=float(input("Humano inresa el numero 2: "))
suma=n1+n2
resta=n1-n2
mul=n1*n2
div=n1/n2
divEntera=n1//n2
print(f"Suma={suma}")
print(f"Resta={resta}")
print(f"Multiplicacion={mul}")
print(f"Divicion={div}")
print(f"Divicion entera={divEntera}")
