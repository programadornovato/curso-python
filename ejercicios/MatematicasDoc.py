def suma(a,b):
    '''Esta funcion suma los parametros a y b'''
    return a+b
def resta(a,b):
    '''Esta funcion resta los parametros a y b'''
    return a-b
def mul(a,b):
    '''Esta funcion multiplicacion los parametros a y b'''
    return a*b
def div(a,b):
    '''Esta funcion divicion los parametros a y b'''
    return a/b
